package entidades;

public class ArbolDistribucion {
	
	//Atributos
	private int Arbol_DistribucionId;
	private int ArbolId;
	private int DistribucionId;

	//Metodos
	public int getArbol_DistribucionId() {
		return Arbol_DistribucionId;
	}
	public void setArbol_DistribucionId(int arbol_DistribucionId) {
		Arbol_DistribucionId = arbol_DistribucionId;
	}
	public int getArbolId() {
		return ArbolId;
	}
	public void setArbolId(int arbolId) {
		ArbolId = arbolId;
	}
	public int getDistribucionId() {
		return DistribucionId;
	}
	public void setDistribucionId(int distribucionId) {
		DistribucionId = distribucionId;
	}
	
}
