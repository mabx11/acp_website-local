package vistas;

public class VW_DistribucionArbol {
	
	//Atributos
	private int ID;
	private int ArbolId;
	private String NombreArbol;
	private int Distribucionid;
	private String NombreDistribucion;
	
	//Metodos
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public int getArbolId() {
		return ArbolId;
	}
	public void setArbolId(int arbolId) {
		ArbolId = arbolId;
	}
	public String getNombreArbol() {
		return NombreArbol;
	}
	public void setNombreArbol(String nombreArbol) {
		NombreArbol = nombreArbol;
	}
	public int getDistribucionid() {
		return Distribucionid;
	}
	public void setDistribucionid(int distribucionid) {
		Distribucionid = distribucionid;
	}
	public String getNombreDistribucion() {
		return NombreDistribucion;
	}
	public void setNombreDistribucion(String nombreDistribucion) {
		NombreDistribucion = nombreDistribucion;
	}
	
}
