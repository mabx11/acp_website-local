package vistas;

public class VW_Arbol {

	//Atributos
	private int ID;
	private String NombreCientifico;
	private String NombreComun;
	private String Descripcion;
	private int Estado;
	private String Familia;
	private String Genero;
	private String EpocaFloracion;
	private String DescripDistribucion;
	private String Imagen;
	
	//Metodos
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getNombreCientifico() {
		return NombreCientifico;
	}
	public void setNombreCientifico(String nombreCientifico) {
		NombreCientifico = nombreCientifico;
	}
	public String getNombreComun() {
		return NombreComun;
	}
	public void setNombreComun(String nombreComun) {
		NombreComun = nombreComun;
	}
	public String getDescripcion() {
		return Descripcion;
	}
	public void setDescripcion(String descripcion) {
		Descripcion = descripcion;
	}
	public int getEstado() {
		return Estado;
	}
	public void setEstado(int estado) {
		Estado = estado;
	}
	public String getFamilia() {
		return Familia;
	}
	public void setFamilia(String familia) {
		Familia = familia;
	}
	public String getGenero() {
		return Genero;
	}
	public void setGenero(String genero) {
		Genero = genero;
	}
	public String getEpocaFloracion() {
		return EpocaFloracion;
	}
	public void setEpocaFloracion(String epocaFloracion) {
		EpocaFloracion = epocaFloracion;
	}
	public String getDescripDistribucion() {
		return DescripDistribucion;
	}
	public void setDescripDistribucion(String nombreDistribucion) {
		DescripDistribucion = nombreDistribucion;
	}
	
	public String getImagen() {
		return Imagen;
	}
	public void setImagen(String imagen) {
		Imagen = imagen;
	}
	
	
}
